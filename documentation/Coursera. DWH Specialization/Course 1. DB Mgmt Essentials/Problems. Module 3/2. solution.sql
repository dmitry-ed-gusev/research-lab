-- Create necessary tables
CREATE TABLE Employee (
    empno      VARCHAR(8) PRIMARY KEY,
    empname    VARCHAR(50) CONSTRAINT EmpNameRequired NOT NULL,
    department VARCHAR(25) CONSTRAINT DepartmentNameRequired NOT NULL,
    email      VARCHAR(30) CONSTRAINT EmailRequired NOT NULL,
    phone      VARCHAR(11) CONSTRAINT EmployeePhoneRequired NOT NULL,
    mgrno      VARCHAR(8), 
    CONSTRAINT EmailUnique UNIQUE (email),
    CONSTRAINT EmployeeSelfFK FOREIGN KEY (mgrno) REFERENCES Employee (empno)
);

CREATE TABLE Customer (
    custno   VARCHAR(8) PRIMARY KEY,
    custname VARCHAR(50) CONSTRAINT CustNameRequired NOT NULL,
    address  VARCHAR(50) CONSTRAINT AddressRequired NOT NULL,
    internal CHAR(1) DEFAULT 'Y' CONSTRAINT InternalRequired NOT NULL,
    contact  VARCHAR(35) CONSTRAINT ContactRequired NOT NULL,
    phone    VARCHAR(11) CONSTRAINT CustomerPhoneRequired NOT NULL,
    city     VARCHAR(30) CONSTRAINT CityRequired NOT NULL,
    state    CHAR(2) CONSTRAINT StateRequired NOT NULL,
    zip      VARCHAR(10) DEFAULT '80202' CONSTRAINT ZipRequired NOT NULL
);

CREATE TABLE Facility (
    facno   VARCHAR(8) PRIMARY KEY,
    facname VARCHAR(50) CONSTRAINT FacNameRequired NOT NULL,
    CONSTRAINT FacNameUnique UNIQUE (facname) 
);

CREATE TABLE Location (
    locno   VARCHAR(8) PRIMARY KEY,
    facno   VARCHAR(8) CONSTRAINT FacNoRequired NOT NULL,
    locname VARCHAR(30) CONSTRAINT LocNameRequired NOT NULL,
    CONSTRAINT LocationFacilityFK FOREIGN KEY (facno) REFERENCES Facility (facno)
);

CREATE TABLE ResourceTbl (
    resno   VARCHAR(8) PRIMARY KEY,
    resname VARCHAR(30) CONSTRAINT ResNameRequired NOT NULL,
    rate    DECIMAL(10, 2) DEFAULT 1 CONSTRAINT RateRequired NOT NULL,
    CONSTRAINT ResNameUnique UNIQUE (resname),
    CONSTRAINT ValidRate CHECK (rate > 0)
);

CREATE TABLE EventRequest (
    eventno     VARCHAR(8) PRIMARY KEY,
    dateheld    DATE CONSTRAINT DateHeldRequired NOT NULL,
    datereq     DATE DEFAULT CURRENT_DATE CONSTRAINT DateReqRequired NOT NULL,
    custno      VARCHAR(8) CONSTRAINT CustomerNoRequired NOT NULL,
    facno       VARCHAR(8) CONSTRAINT FacilityNoRequired NOT NULL,
    dateauth    DATE,
    status      VARCHAR(8) DEFAULT 'PENDING' CONSTRAINT StatusRequired NOT NULL,
    estcost     DECIMAL(10, 2) CONSTRAINT EstCostRequired NOT NULL,
    estaudience INTEGER CONSTRAINT EstAudienceRequired NOT NULL,
    budno       VARCHAR(8),
    CONSTRAINT CustomerFK FOREIGN KEY (custno) REFERENCES Customer (custno),
    CONSTRAINT EventFacilityFK FOREIGN KEY (facno) REFERENCES Facility (facno),
    CONSTRAINT ValidEstAudience CHECK (estaudience > 0),
    CONSTRAINT ValidStatus CHECK (status IN ('PENDING', 'APPROVED', 'DENIED', 'Pending', 'Approved', 'Denied'))
);

CREATE TABLE EventPlan (
    planno   VARCHAR(8) PRIMARY KEY,
    eventno  VARCHAR(8) CONSTRAINT EventNoRequired NOT NULL,
    workdate DATE CONSTRAINT WorkDateRequired NOT NULL,
    notes    VARCHAR(50),
    activity VARCHAR(50) CONSTRAINT ActivityRequired NOT NULL,
    empno    VARCHAR(8),
    CONSTRAINT EventRequestFK FOREIGN KEY (eventno) REFERENCES EventRequest (eventno) ON DELETE CASCADE,
    CONSTRAINT EmployeeEventFK FOREIGN KEY (empno) REFERENCES Employee (empno)
);

CREATE TABLE EventPlanLine (
    planno      VARCHAR(8),
    lineno      VARCHAR(8),
    timestart   DATE CONSTRAINT TimeStartRequired NOT NULL,
    timeend     DATE CONSTRAINT TimeEndRequired NOT NULL,
    resourcecnt INTEGER CONSTRAINT ResourceCntRequired NOT NULL,
    locno       VARCHAR(8) CONSTRAINT LocNoRequired NOT NULL,
    resno       VARCHAR(8) CONSTRAINT ResNoRequired NOT NULL,
    CONSTRAINT EventPlanLanePK PRIMARY KEY (planno, lineno),
    CONSTRAINT EventPlanFK FOREIGN KEY (planno) REFERENCES EventPlan (planno) ON DELETE CASCADE,
    CONSTRAINT LocationFK FOREIGN KEY (locno) REFERENCES Location (locno),
    CONSTRAINT ResourceFK FOREIGN KEY (resno) REFERENCES ResourceTbl (resno),
    CONSTRAINT ValidStartAndEndTime CHECK (timestart < timeend)
);

